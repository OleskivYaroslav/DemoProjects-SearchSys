package view;

public class BeanClass {
    
    private int x;
    private int y;
    
    
    
    
    public BeanClass() {
    }


    public void setX(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getY() {
        return y;
    }
}
